# if , else , elif
# if = se
# else = se não
# elif = se não se


entrada = input('Digite sim ou não: ')
if entrada == ('Sim') or entrada == ('sim'):
    print('Você digitou Sim')
elif entrada == ('Não') or entrada == ('não'):
    print('Você digitou Não')
else:
    print('Você digitou algo diferente de Sim ou Não')