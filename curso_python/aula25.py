sobrenome = 'Alves da silva'
print(sobrenome[4])
print(sobrenome[0:3])
print(sobrenome[2:5])
print(len(sobrenome)) # conta os caracteres da string
print(sobrenome[0:9:2]) # o terceiro número é o passo, ou seja, ele vai pegar um caractere a cada 2 caracteres
print(sobrenome[::-1])# o primeiro número é o início, o segundo é o fim e o terceiro é o passo, ou seja, ele vai pegar um caractere a cada 3 caracteres
