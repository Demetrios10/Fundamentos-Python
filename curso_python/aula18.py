# Opredaores de Comparação (relacionais)

'''
> maior
< menor
>= maior ou igual
<= menor ou igual
== igual
!= diferente
'''


print(2 > 10)
print(10 < 200)
print(1000 >= 1000)
print(200 <= 20)
print(10 == 10)
print("Demetrios" != 'Deltas')


print('Deltas' == 'Deltassss')
print('nome' != 'Nomes')






